        STAT = 'S' | ('T' << 8) | ('A' << 16) | ('T' << 24),
        SPRV = 'S' | ('P' << 8) | ('R' << 16) | ('V' << 24),
        FLCI = 'F' | ('L' << 8) | ('C' << 16) | ('I' << 24),
        DSTF = 'D' | ('S' << 8) | ('T' << 16) | ('F' << 24),
        TBUF = 'T' | ('B' << 8) | ('U' << 16) | ('F' << 24),
        MVTX = 'M' | ('V' << 8) | ('T' << 16) | ('X' << 24),
        MIDX = 'M' | ('I' << 8) | ('D' << 16) | ('X' << 24),
#pragma warning restore CS1591
    }
}
